        std::cout << "We'ld jump the life to come. But in these cases";
        break;
    case 8:
        std::cout << "We still have judgment here; that we but teach";
        break;
    case 9:
        std::cout << "Bloody instructions, which, being taught, return";
        break;
    case 10:
        std::cout << "To plague the inventor: this even-handed justice";
        break;
    case 11:
        std::cout << "Commends the ingredients of our poison'd chalice";
        break;
    case 12:
        std::cout << "To our own lips. He's here in double trust;";
        break;
    case 13:
        std::cout << "First, as I am his kinsman and his subject,";
        break;
    case 14:
        std::cout << "Strong both against the deed; then, as his host,";
